//for loop
for (let i = 0; i <= 5; i++) {
  if (i % 2 !== 0) console.log("i " + i); //printing odd nos from 1 to 5
}

//while loop
let y = 5;
while (y >= 0) {
  console.log("y:" + y);
  y--;
}

//do while
//will be executed atleast once
let x = -1;
do {
  console.log("x:" + x);
  x--;
} while (x >= 0);
